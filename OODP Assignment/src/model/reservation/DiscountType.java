package model.reservation;

/**
 * DiscountType is an enumeration that specifies the possible types of discount.
 * @author YingHao
 */
public enum DiscountType {
	
	/**
	 * Specifies that a discount is to be calculated in percentage.
	 */
	PERCENTAGE,
	
	/**
	 * Specifies that a discount is to be calculated as a fixed amount.
	 */
	FIXED;

}
